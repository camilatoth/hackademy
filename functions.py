def convert_to_seconds(hours, minutes, seconds):
	hours_convert = hours * 3600
	
	minutes_convert = minutes * 60
	
	seconds_convert = seconds
	
	total_convert = hours_convert + minutes_convert + seconds_convert
	
	return total_convert

def convert_to_inches(feet,inches):
	feet_convert = feet * 12
	
	inches_convert = inches
	
	total_convert = feet_convert + inches_convert 

	return total_convert

